@app.route("/api/policy/evaluate", methods=["GET"])
@jwt_required()
def evaluate_policy():
    """Evaluate the current reports against the security policy."""
    policy = Policy.get_instance()

    sast_data = load_json_file("sast-report.json")
    bandit_data = load_json_file("bandit-report.json")
    trivy_data = load_json_file("trivy-report.json")
    gitleaks_data = load_json_file("gitleaks-report.json")
    dast_data = load_json_file("dast-report.json")

    # ---- SAST score ----
    sast_score = 100
    sast_critical = 0
    sast_high = 0
    sast_medium = 0
    sast_low = 0

    if sast_data and sast_data.get("metrics"):
        totals = sast_data["metrics"].get("totals", {})
        sast_critical = totals.get("critical", 0)
        sast_high = totals.get("high", 0)
        sast_medium = totals.get("medium", 0)
        sast_low = totals.get("low", 0)
    elif bandit_data and bandit_data.get("results"):
        for r in bandit_data["results"]:
            sev = r.get("issue_severity", "").upper()
            if sev == "HIGH":
                sast_high += 1
            elif sev == "MEDIUM":
                sast_medium += 1
            else:
                sast_low += 1

    sast_score -= sast_critical * 15 + sast_high * 8 + sast_medium * 3 + sast_low * 1
    sast_score = max(0, sast_score)

    # ---- Trivy score ----
    trivy_score = 100
    trivy_critical = 0
    trivy_high = 0
    trivy_medium = 0
    trivy_low = 0

    if trivy_data and trivy_data.get("Results"):
        for result in trivy_data["Results"]:
            for vuln in result.get("Vulnerabilities", []):
                sev = vuln.get("Severity", "").upper()
                if sev == "CRITICAL":
                    trivy_critical += 1
                elif sev == "HIGH":
                    trivy_high += 1
                elif sev == "MEDIUM":
                    trivy_medium += 1
                else:
                    trivy_low += 1
    trivy_score -= trivy_critical * 15 + trivy_high * 8 + trivy_medium * 3 + trivy_low * 1
    trivy_score = max(0, trivy_score)

    # ---- Gitleaks score ----
    gitleaks_score = 100
    gitleaks_total = 0
    gitleaks_critical = 0
    gitleaks_high = 0

    if gitleaks_data:
        gitleaks_total = gitleaks_data.get("total_secrets", 0)
        gitleaks_critical = gitleaks_data.get("metrics", {}).get("critical", 0)
        gitleaks_high = gitleaks_data.get("metrics", {}).get("high", 0)
    gitleaks_score -= gitleaks_critical * 20 + gitleaks_high * 12 + (gitleaks_total - gitleaks_critical - gitleaks_high) * 5
    gitleaks_score = max(0, gitleaks_score)

    # ---- DAST score ----
    dast_score = 100
    dast_high = 0
    dast_medium = 0
    dast_low = 0

    if dast_data:
        dast_high = dast_data.get("metrics", {}).get("high", 0)
        dast_medium = dast_data.get("metrics", {}).get("medium", 0)
        dast_low = dast_data.get("metrics", {}).get("low", 0)
    dast_score -= dast_high * 10 + dast_medium * 4 + dast_low * 1
    dast_score = max(0, dast_score)

    # ---- Composite ----
    weights = {"sast": 0.35, "trivy": 0.25, "gitleaks": 0.25, "dast": 0.15}
    composite = round(
        sast_score * weights["sast"]
        + trivy_score * weights["trivy"]
        + gitleaks_score * weights["gitleaks"]
        + dast_score * weights["dast"]
    )

    total_critical = sast_critical + trivy_critical + gitleaks_critical
    total_high = sast_high + trivy_high + dast_high

    # ---- Decision ----
    is_deployable = True
    reasons = []

    if composite < policy.min_score:
        is_deployable = False
        reasons.append(f"Security score {composite} below minimum {policy.min_score}")
    if policy.block_critical and total_critical > policy.max_critical_vulns:
        is_deployable = False
        reasons.append(f"{total_critical} critical vulnerabilities (max {policy.max_critical_vulns})")
    if policy.block_high and total_high > policy.max_high_vulns:
        is_deployable = False
        reasons.append(f"{total_high} high vulnerabilities (max {policy.max_high_vulns})")

    return jsonify({
        "security_score": composite,
        "is_deployable": is_deployable,
        "reasons": reasons,
        "scores": {
            "sast": sast_score,
            "trivy": trivy_score,
            "gitleaks": gitleaks_score,
            "dast": dast_score,
        },
        "vulnerability_summary": {
            "critical": total_critical,
            "high": total_high,
            "medium": sast_medium + trivy_medium + dast_medium,
            "low": sast_low + trivy_low + dast_low,
        },
        "policy": policy.to_dict(),
        "evaluated_at": utcnow().isoformat(),
    })


