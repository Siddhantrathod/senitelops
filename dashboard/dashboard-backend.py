@app.route("/api/auth/google/callback", methods=["GET"])
def google_callback():
    """Handle the OAuth callback from Google."""
    if not OAUTH_AVAILABLE:
        return jsonify({"error": "OAuth library not installed"}), 500

    code = request.args.get("code")
    if not code:
        return redirect(f"{FRONTEND_URL}/login?error=google_auth_failed")

    try:
        oauth_client = OAuth2Session(
            client_id=GOOGLE_CLIENT_ID,
            client_secret=GOOGLE_CLIENT_SECRET,
            redirect_uri=request.url_root.rstrip("/") + "/api/auth/google/callback",
            state=session.get("oauth_state"),
        )
        oauth_client.fetch_token(GOOGLE_TOKEN_URI, code=code)
        resp = oauth_client.get(GOOGLE_USERINFO_URI)
        google_user = resp.json()

        google_email = google_user.get("email", "")
        google_name = google_user.get("name", "")
        google_sub = google_user.get("sub", "")

        if not google_email:
            return redirect(f"{FRONTEND_URL}/login?error=no_email")

        existing = User.query.filter(db.func.lower(User.email) == google_email.lower()).first()

        if existing:
            existing.last_login_at = utcnow()
            if not existing.auth_provider:
                existing.auth_provider = "google"
            db.session.commit()
            user = existing
        else:
            # Generate unique username
            base_username = google_email.split("@")[0]
            username = base_username
            counter = 1
            while User.query.filter(db.func.lower(User.username) == username.lower()).first():
                username = f"{base_username}{counter}"
                counter += 1

            user = User(
                username=username,
                email=google_email,
                password_hash="",
                role="viewer",
                full_name=google_name,
                auth_provider="google",
                google_id=google_sub,
                last_login_at=utcnow(),
            )
            db.session.add(user)
            db.session.commit()

        access_token = create_access_token(
            identity=str(user.id),
            additional_claims={
                "username": user.username,
                "email": user.email,
                "role": user.role,
            },
        )
        return redirect(f"{FRONTEND_URL}/login?token={access_token}&provider=google")

    except Exception as e:
        app.logger.error(f"Google OAuth error: {e}")
        return redirect(f"{FRONTEND_URL}/login?error=oauth_error&message={str(e)}")


